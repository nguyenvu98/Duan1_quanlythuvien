    DanhMucSachDAO dao = new DanhMucSachDAO();
    int row = -1;

    private void innit() {
        this.setLocationRelativeTo(null);

        this.filltable();
        this.row = -1;
        this.updateStatus();
    }

    void edit() {
        String MaDMS = (String) tbldanhmucsasch.getValueAt(this.row, 0);
        DanhMucSach nxb = dao.selectById(MaDMS);
        this.setForm(nxb);
        Tabs.setSelectedIndex(0);
        this.updateStatus();
    }

    void filltable() {
        DefaultTableModel model = (DefaultTableModel) tbldanhmucsasch.getModel();
        model.setRowCount(0);
        try {
            List<DanhMucSach> list = dao.selectAll();
            for (DanhMucSach nxb : list) {
                Object[] row = {
                    nxb.getMaDanhMucSach(),
                    nxb.getTenDanhMucSach(),};
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu");
        }
    }

    void setForm(DanhMucSach nxb) {
        txtMaDanhMucSach.setText(nxb.getMaDanhMucSach());
        txttendanhmucsach.setText(nxb.getTenDanhMucSach());
    }

    void clear() {
        DanhMucSach nxb = new DanhMucSach();
        this.setForm(nxb);
        this.row = -1;
        this.updateStatus();
    }

    DanhMucSach getForm() {
        DanhMucSach nxb = new DanhMucSach();
        nxb.setMaDanhMucSach(txtMaDanhMucSach.getText());
        nxb.setTenDanhMucSach(txttendanhmucsach.getText());
        return nxb;
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tbldanhmucsasch.getRowCount() - 1);

        txtMaDanhMucSach.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnXoa.setEnabled(edit);
        btnSua.setEnabled(edit);

        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    void first() {
        this.row = 0;
        this.edit();
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void next() {
        if (this.row < tbldanhmucsasch.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    void last() {
        this.row = tbldanhmucsasch.getRowCount() - 1;
        this.edit();
    }

    void insert() {
        DanhMucSach nxb = getForm();
        try {
            dao.insert(nxb);
            this.filltable();
            this.clear();
            MsgBox.alert(this, "Succes!");
        } catch (Exception e) {
            MsgBox.alert(this, "Eror!");
        }
    }

    void update() {
        DanhMucSach nxb = getForm();
        try {
            dao.update(nxb);
            this.filltable();
            MsgBox.alert(this, "Succes!");
        } catch (Exception e) {
            MsgBox.alert(this, "Error!");
        }
    }

    void delete() {
        String manv = txtMaDanhMucSach.getText();
        try {
            dao.delete(manv);
            this.filltable();
            this.clear();
            MsgBox.alert(this, "Succes!");
        } catch (Exception e) {
            MsgBox.alert(this, "Error!");
        }
    }
